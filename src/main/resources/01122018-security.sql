use directs;

DROP TABLE IF EXISTS `AUTHORITY`;

CREATE TABLE `AUTHORITY` (
  `ID` INT(10) UNSIGNED NOT NULL AUTO_INCREMENT,
  `NAME` varchar(255) DEFAULT NULL,
  PRIMARY KEY (`ID`),
  UNIQUE KEY `UK_UNIQUE_NAME` (`NAME`)
) ENGINE = InnoDB DEFAULT CHARACTER SET = utf8;


DROP TABLE IF EXISTS `USER`;

CREATE TABLE `USER` (
  `ID` INT(10) UNSIGNED NOT NULL AUTO_INCREMENT,
  `PASSWORD` varchar(255) DEFAULT NULL,
  `USER_NAME` varchar(255) DEFAULT NULL,
  `ACCOUNT_EXPIRED` BOOLEAN,
  `ACCOUNT_LOCKED` BOOLEAN,
  `CREDENTIALS_EXPIRED` BOOLEAN,
  `ENABLED` BOOLEAN,
  PRIMARY KEY (`ID`),
  UNIQUE KEY `UK_UNIQUE_USER_NAME` (`USER_NAME`)
) ENGINE = InnoDB DEFAULT CHARACTER SET = utf8;

DROP TABLE IF EXISTS `USERS_AUTHORITIES`;

CREATE TABLE `USERS_AUTHORITIES` (
  `USER_ID` INT(10) UNSIGNED NOT NULL,
  `AUTHORITY_ID` INT(10) UNSIGNED NOT NULL,
  CONSTRAINT `USERS_AUTHORITIES_AUTHORITY`
    FOREIGN KEY (`AUTHORITY_ID`)
    REFERENCES `directs`.`AUTHORITY` (`ID`)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION,
  CONSTRAINT `USERS_AUTHORITIES_USER`
    FOREIGN KEY (`USER_ID`)
    REFERENCES `directs`.`USER` (`ID`)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION
) ENGINE = InnoDB DEFAULT CHARACTER SET = utf8;


CREATE TABLE OAUTH_CLIENT_DETAILS (
  CLIENT_ID VARCHAR(255) PRIMARY KEY,
  RESOURCE_IDS VARCHAR(255),
  CLIENT_SECRET VARCHAR(255),
  SCOPE VARCHAR(255),
  AUTHORIZED_GRANT_TYPES VARCHAR(255),
  WEB_SERVER_REDIRECT_URI VARCHAR(255),
  AUTHORITIES VARCHAR(255),
  ACCESS_TOKEN_VALIDITY INTEGER,
  REFRESH_TOKEN_VALIDITY INTEGER,
  ADDITIONAL_INFORMATION VARCHAR(4096),
  AUTOAPPROVE VARCHAR(255)
);

DROP TABLE IF EXISTS `OAUTH_CLIENT_TOKEN`;

CREATE TABLE OAUTH_CLIENT_TOKEN (
  TOKEN_ID VARCHAR(255),
  TOKEN BLOB,
  AUTHENTICATION_ID VARCHAR(255) PRIMARY KEY,
  USER_NAME VARCHAR(255),
  CLIENT_ID VARCHAR(255)
);

DROP TABLE IF EXISTS `OAUTH_ACCESS_TOKEN`;

CREATE TABLE OAUTH_ACCESS_TOKEN (
  TOKEN_ID VARCHAR(255),
  TOKEN BLOB,
  AUTHENTICATION_ID VARCHAR(255) PRIMARY KEY,
  USER_NAME VARCHAR(255),
  CLIENT_ID VARCHAR(255),
  AUTHENTICATION BLOB,
  REFRESH_TOKEN VARCHAR(255)
);

DROP TABLE IF EXISTS `OAUTH_REFRESH_TOKEN`;

CREATE TABLE OAUTH_REFRESH_TOKEN (
  TOKEN_ID VARCHAR(255),
  TOKEN BLOB,
  AUTHENTICATION BLOB
);

DROP TABLE IF EXISTS `OAUTH_CODE`;

CREATE TABLE OAUTH_CODE (
  CODE VARCHAR(255),
  AUTHENTICATION BLOB
);

CREATE TABLE OAUTH_APPROVALS (
  USERID VARCHAR(255),
  CLIENTID VARCHAR(255),
  SCOPE VARCHAR(255),
  STATUS VARCHAR(10),
  EXPIRESAT TIMESTAMP,
  LASTMODIFIEDAT TIMESTAMP
);


